import type { Metadata } from "next";
import "./globals.css";
import GlobalNav from "./component/global-nav";
import NoteInspector from "./component/note-inspector";
import { pretendard } from "./lib/localfont";
import ApolloWrapper from "./lib/ApolloWrapper";
import { Suspense } from 'react';
import { gql, GraphQLClient } from "graphql-request";
import { Tag, TagsResponse } from "./lib/type";
import { ThemeProvider } from "next-themes";

const client = new GraphQLClient(process.env.GRAPHQL_API_URL!);

const GET_ALL_TAGS = gql`
  query {
    tags {
      id
      name
    }
  }
`;

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const data: TagsResponse = await client.request(GET_ALL_TAGS);
  const tags: Tag[] = data.tags;

  return (
    <html lang="en" suppressHydrationWarning>
      <body
        className={`${pretendard.className} antialiased font-normal overflow-hidden`}
      >
        <ThemeProvider disableTransitionOnChange>
          <div className="flex h-screen w-full p-8 gap-4">
            <aside className="fixed h-full w-80 shrink-0">
              <Suspense>
                <GlobalNav />
              </Suspense>
              <NoteInspector tags={tags} />
            </aside>
            <main className="absolute left-[21rem] flex flex-col h-full w-[calc(100vw-21rem)] flex-1 items-center overflow-hidden">
              <ApolloWrapper>
                {children}
              </ApolloWrapper>
            </main>
          </div>
        </ThemeProvider>
      </body>
    </html>
  );
}